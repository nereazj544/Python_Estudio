# Operadores
print("Operadores aritméticos")

a = 1+9
b= 8-9
c = 2*3
d = 10/2 # Division en Python siempre devuelve un float
e = 10//2 # Division entera, devuelve un int
f = 10%3 # Resto de la division
g = 2**3 # Potencia, 2 elevado a 3
print(f"suma: {a}, resta: {b}, multiplicacion: {c}, division: {d}, division entera: {e}, resto: {f}, potencia elevada a 3: {g}")

print('Float Point Number, número de punto flotante')
print('Pi', 3.14)

print('Multiplicacion complejo numeros', (2 + 3j) * (6 + 9j))


# operadores de comparacion
print("\nOperadores de comparación")

print(3>2) # Mayor que, sera verdadero
print(3<2) # Menor que, sera falso
print(3>=2) # Mayor o igual que, sera verdadero
print(3<=2) # Menor o igual que, sera falso
print(3==2) # Igual que, sera falso
print(3!=2) # Diferente que, sera verdadero

# adicional 
