# Cadenas  de texto, se escriben de dos formas
print ("Forma 1: usando comillas dobles Hello, World!")
print ('Forma 2: usando comillas simples ¡Hola, Mundo!')

# Saber un tipo de dato
print("\ntipos de datos:")
print(type("dato str"))  # Cuando esto se ejecute, se imprimirá <class 'str'>, que significa cadena de texto
print(type(3))  # Cuando esto se ejecute, se imprimirá <class 'int'>, que significa número entero
print(type(12.3))  # Cuando esto se ejecute, se imprimirá <class 'float'>, que significa número decimal
print(type(True))  # Cuando esto se ejecute, se imprimirá <class 'bool'>, que significa booleano
print(type(None))  # Cuando esto se ejecute, se imprimirá <class 'NoneType'>, que significa nulo
print(type(2j))  # Cuando esto se ejecute, se imprimirá <class 'complex'>, que significa número complejo
